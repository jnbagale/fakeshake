#include <sp.h>
#include <glib.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>


#include "fake.h"
#include "comms.h"
#include "config.h"



gint write_message(fakeObject *fake_obj, gchar *msg)
{
  gint ret;
  guint msg_len;
   
  msg_len = strlen(msg);
 
  ret = SP_multicast(fake_obj->mbox, UNRELIABLE_MESS, fake_obj->group_name, 1, msg_len, msg);
  
  return(ret);  
  
}
